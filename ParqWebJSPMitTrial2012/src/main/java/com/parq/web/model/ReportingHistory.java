package com.parq.web.model;

import java.util.Date;

public class ReportingHistory {
	private Date date;
	private int points;
	
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	public int getPoints() {
		return points;
	}
	public void setPoints(int points) {
		this.points = points;
	}
	
	
}
